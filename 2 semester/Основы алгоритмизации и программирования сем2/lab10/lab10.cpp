#include <iostream>
#include <cmath>

using namespace std;

// Функция nestedSqrt используется для рекурсивного вычисления значения формулы вложенных квадратных корней
double nestedSqrt(int n, int k) {
    // Если достигнут конечный уровень вложенности (k == n), возвращаем значение корня из соответствующей суммы
    if (k == n) {
        return sqrt(1 + (n + n));
    }
    // Иначе вычисляем значение внутреннего корня и рекурсивно вызываем эту же функцию для следующего уровня вложенности
    return sqrt(1 + (n + k) * nestedSqrt(n, k + 1));
}

// Функция main является точкой входа в программу
int main() {
    // Задаём начальное значение переменной n
    int n = 5;
    // Вычисляем значение формулы, используя рекурсивную функцию nestedSqrt
    double result = sqrt(1 + (n + 1) * nestedSqrt(n, 2));
    // Выводим результат на экран
    cout << "Result: " << result << endl;
    return 0;
}
